//get div element from html with class .frame
//divide it with grid as many colums as many dimension has image(each color represents as column)
let frame = document.querySelector('.frame')



function createFrame(num) {
    frame.style.gridTemplateColumns = `repeat( ${num}, 1fr)`
    frame.style.width = `${num * 20}px`
    for (let i = 0; i < num * num; i++) {
        let px = document.createElement('div')
        frame.appendChild(px)
    }
}

function render(image) {
    var pxs = frame.children
    for (let i = 0; i < image.length; i++) {
        setTimeout(() => {
            for (let j = 0; j < image[i].length; j++) {
                pxs[j].style.backgroundColor = image[i][j]
                console.log(image[i][j])
            }

        }, `${i}000`);

    }
}
